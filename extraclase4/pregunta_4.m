function I = cuad_gauss(f, a, b, k)
  % Obtener nodos y pesos
  [x, w] = nodos_pesos(k);

  % Transformación de [-1,1] a [a,b]
  t = ((b - a) / 2) * x + (a + b) / 2;

  % Fórmula del método
  I = (b - a) / 2;
  sum = 0;
  for i = 1:length(w)
    sum = sum + w(i) * f(t(i));
  end
  I = I * sum;
end

function I = cuad_gauss_comp(f, a, b, k, n)
  h = (b - a) / n;   % tamaño de cada subintervalo
  I = 0;

  for i = 0:n-1
    ai = a + i*h;
    bi = ai + h;
    I += cuad_gauss(f, ai, bi, k);
  endfor
end

function I = cuad_gauss_iter(f, a, b, k)
  tol = 1e-8;
  max_n = 1e6;

  n = 1;
  I_prev = cuad_gauss_comp(f, a, b, k, n);

  while true
    n = n * 2;
    if n > max_n
      disp('Se alcanzó el número máximo de subintervalos.');
      break;
    end

    I = cuad_gauss_comp(f, a, b, k, n);

    if abs(I - I_prev) < tol
      break;
    end

    I_prev = I;
  endwhile
end



